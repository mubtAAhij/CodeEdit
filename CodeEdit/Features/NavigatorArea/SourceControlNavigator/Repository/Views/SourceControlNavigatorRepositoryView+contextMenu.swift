import SwiftUI

extension SourceControlNavigatorRepositoryView {
    func handleDelete(_ item: RepoOutlineGroupItem) {
        if item.branch != nil {
            isPresentingConfirmDeleteBranch = true
            branchToDelete = item.branch
        }
        if item.stashEntry != nil {
            isPresentingConfirmDeleteStashEntry = true
            stashEntryToDelete = item.stashEntry
        }
        if item.remote != nil {
            isPresentingConfirmDeleteRemote = true
            remoteToDelete = item.remote
        }
    }

    @ViewBuilder
    func contextMenu(for item: RepoOutlineGroupItem, branch: GitBranch) -> some View {
        Button(String(localized: "source-control.switch-branch", defaultValue: "Switch...", comment: "Context menu item to switch to a branch")) {
            sourceControlManager.switchToBranch = branch
        }
        .disabled(item.branch == nil || sourceControlManager.currentBranch == item.branch)
        Divider()
        Button(
            item.branch == nil && item.id != "BranchesGroup"
            ? String(localized: "source-control.new-branch", defaultValue: "New Branch...", comment: "Context menu item to create a new branch")
            : String(localized: "source-control.new-branch-from", defaultValue: "New Branch from \"\(branch.name)\"...", comment: "Context menu item to create a new branch from an existing branch")
        ) {
            showNewBranch = true
            fromBranch =  item.branch
        }
        .disabled(item.branch == nil && item.id != "BranchesGroup")
        Button(
            item.branch == nil
            ? String(localized: "source-control.rename-branch", defaultValue: "Rename Branch...", comment: "Context menu item to rename a branch")
            : String(localized: "source-control.rename-branch-named", defaultValue: "Rename \"\(branch.name)\"...", comment: "Context menu item to rename a specific branch")
        ) {
            showRenameBranch = true
            fromBranch = item.branch
        }
        .disabled(item.branch == nil || item.branch?.isRemote == true)
        Divider()
        Button(String(localized: "source-control.add-remote", defaultValue: "Add Existing Remote...", comment: "Context menu item to add an existing remote")) {
            sourceControlManager.addExistingRemoteSheetIsPresented = true
        }
        .disabled(item.id != "RemotesGroup")
        Divider()
        Button(String(localized: "source-control.apply-stash", defaultValue: "Apply Stashed Changes...", comment: "Context menu item to apply stashed changes")) {
            applyStashedChangesIsPresented = true
            stashEntryToApply = item.stashEntry
        }
        .disabled(item.stashEntry == nil)
        Divider()
        Button(String(localized: "button.delete", defaultValue: "Delete...", comment: "Button to delete an item")) {
            handleDelete(item)
        }
        .disabled(
            (item.branch == nil
             || item.branch?.isLocal == false
             || sourceControlManager.currentBranch == item.branch)
            && item.stashEntry == nil
            && item.remote == nil
        )
    }
}
